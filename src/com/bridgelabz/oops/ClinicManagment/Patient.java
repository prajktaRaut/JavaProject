package com.bridgelabz.oops.ClinicManagment;

public class Patient {

	long patient_id;
	String patient_name;
	long patient_phoneNumber;
	long patient_age;

	

	public long getPatient_id() {
		return patient_id;
	}

	public void setPatient_id(long patient_id) {
		this.patient_id = patient_id;
	}

	public long getPatient_age() {
		return patient_age;
	}

	public void setPatient_age(long patient_age) {
		this.patient_age = patient_age;
	}

	public String getPatient_name() {
		return patient_name;
	}

	public void setPatient_name(String patient_name) {
		this.patient_name = patient_name;
	}

	public long getPatient_phoneNumber() {
		return patient_phoneNumber;
	}

	public void setPatient_phoneNumber(long patient_phoneNumber) {
		this.patient_phoneNumber = patient_phoneNumber;
	}

	
	
}
